/// Phase 3 전문가 심화 보강 본문 (기존 ID 유지)
final Map<String, FinanceContent> phase3Overrides = {
  for (final c in phase3OverrideList) c.id: c,
};

void assertPhase3OverridesComplete() {
  final missing = phase3UpgradedIds.where(
    (id) => !phase3Overrides.containsKey(id),
  );
  if (missing.isNotEmpty) {
    throw StateError('Phase3 override missing: ${missing.join(', ')}');
  }
}

FinanceContent _practical({
  required String id,
  required String title,
  required String summary,
  required String category,
  required String easy,
  required String calc,
  required String procedure,
  required String risk,
  required String badCase,
  required List<String> checklist,
  List<String> related = const [],
  List<String> sources = const ['src-fss'],
  List<LifeStage> stages = const [LifeStage.employee, LifeStage.midCareer],
  VerificationStatus status = VerificationStatus.educationalExample,
  Difficulty difficulty = Difficulty.expert,
  String reviewDueAt = '2026-10-23',
  List<String> calcBullets = const [],
  String? extraHeading,
  String? extraBody,
  ContentType extraType = ContentType.concept,
}) {
  final sections = <ContentSection>[
    section('쉬운 설명', easy),
    section(
      '원리·판단 흐름',
      calc,
      bullets: calcBullets,
      type: ContentType.calculation,
    ),
    section('실제 점검·대안', procedure, type: ContentType.actionGuide),
    section('위험·스트레스', risk, type: ContentType.risk),
    section('잘못된 판단 사례', badCase, type: ContentType.risk),
    if (extraHeading != null && extraBody != null)
      section(extraHeading, extraBody, type: extraType),
    section(
      '공식 확인·기준일',
      '제도·세율·규제는 변경될 수 있습니다. 공식기관을 재확인하세요. '
          'referenceYear 2026, checkedAt 2026-07-23, reviewDueAt $reviewDueAt.',
      type: ContentType.officialCheck,
    ),
    section(
      '개인정보·교육용 제한',
      '민감 재무정보를 서버에 저장하지 않습니다. 매수·매도 추천·수익 보장을 하지 않습니다. '
          '개인 적용 전 공식자료·전문가 확인이 필요할 수 있습니다.',
      type: ContentType.actionGuide,
    ),
  ];
  return buildLesson(
    id: id,
    title: title,
    summary: summary,
    category: category,
    difficulty: difficulty,
    contentType: ContentType.actionGuide,
    keywords: [title, '전문가', '3단계'],
    relatedIds: related,
    sourceIds: sources,
    lifeStages: stages,
    verificationStatus: status,
    referenceYear: '2026',
    checkedAt: '2026-07-23',
    reviewDueAt: reviewDueAt,
    reviewCycle: '분기 1회',
    checklist: checklist,
    nextActions: const [
      '관련 계산기·투자정책서·연간점검으로 숫자 확인',
      '공식자료·계약서 재확인',
      '개인 상황이면 전문가 상담 검토',
    ],
    commonMistakes: const ['한 지표로 결론', '제도·세율 영구 고정', '개인정보 AI채팅 유출'],
    sections: sections,
  );
}

