  List<LifeStage> stages = const [LifeStage.employee],
  VerificationStatus status = VerificationStatus.educationalExample,
  Difficulty difficulty = Difficulty.practical,
  String reviewDueAt = '2026-10-23',
  List<String> calcBullets = const [],
  String? extraHeading,
  String? extraBody,
  ContentType extraType = ContentType.concept,
}) {
  final sections = <ContentSection>[
    section('쉬운 설명', easy),
    section(
      '확인할 숫자·계산 흐름',
      calc,
      bullets: calcBullets,
      type: ContentType.calculation,
    ),
    section('실제 점검 절차', procedure, type: ContentType.actionGuide),
    section('위험·최악의 경우', risk, type: ContentType.risk),
    section('잘못된 판단 사례', badCase, type: ContentType.risk),
    if (extraHeading != null && extraBody != null)
      section(extraHeading, extraBody, type: extraType),
    section(
      '공식 확인·기준일',
      '제도·세율·규제·보호한도는 변경될 수 있습니다. 공식기관을 재확인하고 필요 시 전문가에게 확인하세요. '
          'referenceYear 2026, checkedAt 2026-07-23, reviewDueAt \$reviewDueAt.',
      type: ContentType.officialCheck,
    ),
    section(
      '개인정보·교육용 제한',
      '민감 재무정보를 서버에 저장하지 않습니다. 특정 상품·종목·부동산 매수·매도 추천이나 수익 보장을 하지 않습니다.',
      type: ContentType.actionGuide,
    ),
  ];
  return buildLesson(
    id: id,
    title: title,
    summary: summary,
    category: category,
    difficulty: difficulty,
    contentType: ContentType.actionGuide,
    keywords: [title, '실무', '2단계'],
    relatedIds: related,
    sourceIds: sources,
    lifeStages: stages,
    verificationStatus: status,
    referenceYear: '2026',
    checkedAt: '2026-07-23',
    reviewDueAt: reviewDueAt,
    reviewCycle: '분기 1회',
    checklist: checklist,
    nextActions: const ['관련 계산기·재무진단으로 숫자 확인', '공식자료·계약서 재확인', '결과는 로컬에만 기록'],
    commonMistakes: const ['한 지표로 결론', '제도·세율 영구 고정 가정', '민감정보 AI채팅 유출'],
