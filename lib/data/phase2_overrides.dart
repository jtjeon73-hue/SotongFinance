/// Phase 2 실무 보강 본문 (기존 ID 유지, catalog에서 덮어씀)
final Map<String, FinanceContent> phase2Overrides = {
  for (final c in phase2OverrideList) c.id: c,
};

void assertPhase2OverridesComplete() {
  final missing = phase2UpgradedIds.where(
    (id) => !phase2Overrides.containsKey(id),
  );
  if (missing.isNotEmpty) {
    throw StateError('Phase2 override missing: ${missing.join(', ')}');
  }
}

FinanceContent _practical({
  required String id,
  required String title,
  required String summary,
  required String category,
  required String easy,
  required String calc,
  required String procedure,
  required String risk,
  required String badCase,
  required List<String> checklist,
  List<String> related = const [],
